<script lang="ts">
// Vue
import {Vue, Component, Watch, Prop} from 'vue-property-decorator';

// Models

// Services & helpers
import { log } from '@/services/logger';
import { json, copyToClipboard } from '@/helpers';

// Features
import { WebBluetooth } from './web-bluetooth';

@Component({})
export default class BluetoothDevice extends Vue {

    private itemperBLE: WebBluetooth = new WebBluetooth();

    public async connect() {
        const toggle: HTMLInputElement = document.getElementById('toggle') as HTMLInputElement;
        /* connect to peripheral, load data and add event listeners */
        try {
            await this.itemperBLE.request();
            await this.itemperBLE.connect();
            await this.itemperBLE.setDeviceCharacteristic();
            const notConnectedEl =
            document.getElementById('not-connected');
            if (notConnectedEl) {
                notConnectedEl.classList.add('hidden');
            }
            const vendorEl = document.getElementById('vendor');
            if (vendorEl) {
                vendorEl.textContent = await this.itemperBLE.readCPUVendor();
            }
            const speedEl = document.getElementById('speed');
            if (speedEl) {
                speedEl.textContent = await this.itemperBLE.readCPUSpeed();
            }
            toggle.addEventListener('change', (e: any) => {
                if (e.target.checked) {
                    toggle.disabled = true;
                    setTimeout(() => {
                        toggle.disabled = false;
                    }, 1000);
                } else {
                    toggle.disabled = true;
                    setTimeout(() => {
                        toggle.disabled = false;
                    }, 1000);
                }
            });
            const containerEl = document.getElementById('container');
            if (containerEl) {
                containerEl.classList.remove('hidden');
            }
        } catch (error) {
            log.debug(error.message);
        }
    }
    public disconnect() {
        this.itemperBLE.disconnect();
        const notConnectedEl = document.getElementById('not-connected');
        if (notConnectedEl) {
            notConnectedEl.classList.remove('hidden');
        }
        const containerEl =
        document.getElementById('container');
        if (containerEl) {
            containerEl.classList.add('hidden');
        }
    }
}
